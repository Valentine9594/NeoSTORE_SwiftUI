import SwiftUI

extension Color{
    @nonobjc static var appMaroon: Color {
        return Color(red: 158/255, green: 1/255, blue: 0/255, opacity: 0.6)
    }
    
    @nonobjc static var appRed: Color {
        return Color(red: 233/255, green: 28/255, blue: 26/255, opacity: 1)
    }
}
